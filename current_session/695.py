class Solution(object):
    def maxAreaOfIsland(self, grid):
        """
        :type grid: List[List[int]]
        :rtype: int
        """
        if sum([sum(r) for r in grid]) == 0: return 0
        def sink(p, q):                            # if pos is valid and is island
            if 0 <= p < len(grid) and 0 <= q < len(grid[p]) and grid[p][q] == 1:
                grid[p][q] = 0                     # "sink" the island
                cnt = 1
                cnt += sink(p+1, q)
                cnt += sink(p-1, q)
                cnt += sink(p, q+1)
                cnt += sink(p, q-1)
                # connected islands will all sink
                return cnt
            else:
                return 0
        res = []
        for i in range(len(grid)):
            for j in range(len(grid[0])):
                if grid[i][j]:
                    res += sink(i, j),
        # print res
        return max(res)

