class Solution(object):
    def __init__(self):
        self.g = None

    def numIslands(self, grid):
        """
        :type grid: List[List[str]]
        :rtype: int
        """
        self.g = grid
        # if sum([sum([r]) for r in self.g]): return 0
        cnt = 0
        for i in range(len(grid)):
            for j in range(len(grid[0])):
                cnt += self.sink(i, j)
        return cnt
    
    def sink(self,r,c):
        if self.g[r][c] == "1":
            self.g[r][c] = "0"
            self.sink(min(len(self.g)-1, r+1),c)
            self.sink(max(0,r-1),c)
            self.sink(r,min(len(self.g[0])-1,c+1))
            self.sink(r,max(0,c-1))
            return 1
        else:
            return 0

