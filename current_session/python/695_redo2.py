class Solution(object):
    def maxAreaOfIsland(self, grid):
        """
        :type grid: List[List[int]]
        :rtype: int
        """
        # print(grid)
        H, W = len(grid), len(grid[0])
        def helper(i, j):
            grid[i][j] = 0
            cnt = 1
            for a, b in [(i+1,j),(i-1,j),(i,j+1),(i,j-1)]:
                if 0<=a<H and 0<=b<W and grid[a][b]:
                    cnt += helper(a,b)
            # print('cnt', cnt)
            return cnt

        maxA = 0
        for i in range(H):
            for j in range(W):
                if grid[i][j]:
                    # print(i, j, grid[i][j])
                    a = helper(i, j)
                    maxA = max(maxA, a)
        return maxA

